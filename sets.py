# set - and frozen sets data collection with sets
# indexing - THERE IS NO INDEXING IN SETS
# sets are unordered
# Syntax: {} brackets for sets

car_parts = {"Wheels", "Doors", "Radio"}
print(car_parts)

# managing data with sets

# add an item to the set
car_parts.add("seat belt")
print(car_parts)

# delete and item from the set
car_parts.remove("Wheels")
print(car_parts)